package entity;

public class FreeboardEntity 
{
	private int boardNo;
	private String boardId;
	private String boardSubject;
	private String boardContext;
	private String boardDate;
	private int boardReRef;
	private int boardReDepth;
	private int boardReOrder;
	public int getBoardNo() {
		return boardNo;
	}
	public void setBoardNo(int boardNo) {
		this.boardNo = boardNo;
	}
	public String getBoardId() {
		return boardId;
	}
	public void setBoardId(String boardId) {
		this.boardId = boardId;
	}
	public String getBoardSubject() {
		return boardSubject;
	}
	public void setBoardSubject(String boardSubject) {
		this.boardSubject = boardSubject;
	}
	public String getBoardContext() {
		return boardContext;
	}
	public void setBoardContext(String boardContext) {
		this.boardContext = boardContext;
	}
	public String getBoardDate() {
		return boardDate;
	}
	public void setBoardDate(String boardDate) {
		this.boardDate = boardDate;
	}
	public int getBoardReRef() {
		return boardReRef;
	}
	public void setBoardReRef(int boardReRef) {
		this.boardReRef = boardReRef;
	}
	public int getBoardReDepth() {
		return boardReDepth;
	}
	public void setBoardReDepth(int boardReDepth) {
		this.boardReDepth = boardReDepth;
	}
	public int getBoardReOrder() {
		return boardReOrder;
	}
	public void setBoardReOrder(int boardReOrder) {
		this.boardReOrder = boardReOrder;
	}
	
	
	
}
